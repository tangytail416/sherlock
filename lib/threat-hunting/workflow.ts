/**
 * Main threat hunting workflow
 */
async function executeThreatHuntWorkflow(
  threatHuntId: string,
  config: Required<ThreatHuntConfig>
): Promise<void> {
  console.log(`[Threat Hunt] Starting workflow for hunt: ${threatHuntId}`);

  // Load threat hunter agent config
  const agentConfig = await loadAgentConfig('threat_hunter');
  if (!agentConfig) {
    throw new Error('Threat hunter agent configuration not found');
  }

  // Initialize AI client
  const aiClient = await createAIClient(config.aiProvider, {
    modelName: config.modelUsed,
  });

  // Load Splunk guide and structure (like investigation agents do)
  const splunkGuide = await loadAllGuides();
  const splunkStructure = await loadSplunkStructure();
  const whitelistedIOCs = await getActiveWhitelistedIOCs();

  console.log(`[Threat Hunt] Loaded Splunk guide (${splunkGuide.length} chars)`);
  console.log(`[Threat Hunt] Loaded Splunk structure (${splunkStructure.length} chars)`);
  console.log(`[Threat Hunt] Loaded ${whitelistedIOCs.length} whitelisted IOCs`);

  if (splunkStructure.length === 0) {
    console.warn(`[Threat Hunt] WARNING: No Splunk structure loaded! Agent may generate incorrect queries.`);
    console.warn(`[Threat Hunt] Please ensure Splunk config has fetched index structure.`);
  }

  // Track what has been investigated to avoid repeats
  const investigatedTopics = new Set<string>();

  // Determine time range for queries
  const timeRange = {
    earliest: config.timeRange?.earliest || '0', // Default to all time
    latest: config.timeRange?.latest || 'now',
  };

  console.log(`[Threat Hunt] Time range: earliest=${timeRange.earliest}, latest=${timeRange.latest}`);

  // Main autonomous hunting loop
  for (let cycle = 0; cycle < config.maxCycles; cycle++) {
    // Check if hunt was stopped
    const huntStatus = await prisma.threatHunt.findUnique({
      where: { id: threatHuntId },
      select: { status: true },
    });

    if (huntStatus?.status !== 'active') {
      console.log(`[Threat Hunt] Hunt ${threatHuntId} is no longer active, stopping`);
      break;
    }

    console.log(`[Threat Hunt] Cycle ${cycle + 1}/${config.maxCycles} - Generating autonomous hunt plan`);

    // Step 1: Have agent create a hunt plan based on Splunk structure
    const huntPlan = await generateHuntPlan(
      aiClient,
      agentConfig,
      splunkGuide,
      splunkStructure,
      investigatedTopics,
      cycle,
      timeRange,
      config.focusAreas,
      whitelistedIOCs
    );

    if (!huntPlan || huntPlan.length === 0) {
      console.log(`[Threat Hunt] Agent couldn't generate hunt plan, ending hunt`);
      break;
    }

    console.log(`[Threat Hunt] Agent generated plan with ${huntPlan.length} hunt objectives`);

    let cycleFindings = 0;

    // Step 2: Execute each hunt objective in the plan
    for (const objective of huntPlan) {
      try {
        console.log(`[Threat Hunt] Executing: ${objective.name}`);

        // Agent generates query, executes it, and analyzes results
        const result = await executeHuntObjective(
          aiClient,
          agentConfig,
          objective,
          splunkGuide,
          splunkStructure,
          config.maxFindingsPerPattern,
          timeRange,
          whitelistedIOCs
        );

        // Mark this topic as investigated
        investigatedTopics.add(objective.id);

        if (!result || result.findings.length === 0) {
          console.log(`[Threat Hunt] No suspicious activities found for: ${objective.name}`);
          continue;
        }

        const { findings, query } = result;
        console.log(`[Threat Hunt] Agent identified ${findings.length} suspicious activities`);
        console.log(`[Threat Hunt] Query used: ${query.substring(0, 100)}...`);

        // Deduplicate findings
        const newFindings = await deduplicateAndProcessFindings(
          threatHuntId,
          objective.id,
          findings
        );

        if (newFindings.length === 0) {
          console.log(`[Threat Hunt] All findings were duplicates`);
          continue;
        }

        console.log(`[Threat Hunt] ${newFindings.length} new findings after deduplication`);

        // Save findings and spawn investigations
        for (const finding of newFindings) {
          await processFinding(threatHuntId, finding, config, query, objective);
          cycleFindings++;
        }
      } catch (error: any) {
        console.error(`[Threat Hunt] Error executing objective ${objective.name}: ${error.message}`);
        // Continue with next objective
      }
    }

    // Update hunt record
    await prisma.threatHunt.update({
      where: { id: threatHuntId },
      data: {
        cyclesRun: cycle + 1,
        lastRunAt: new Date(),
      },
    });

    console.log(`[Threat Hunt] Cycle ${cycle + 1} complete. Found ${cycleFindings} new findings.`);

    // Wait before next cycle (unless this is the last cycle)
    if (cycle < config.maxCycles - 1) {
      console.log(
        `[Threat Hunt] Waiting ${config.cycleIntervalSeconds} seconds before next cycle`
      );
      await sleep(config.cycleIntervalSeconds * 1000);
    }
  }

  // Mark hunt as completed
  await prisma.threatHunt.update({
    where: { id: threatHuntId },
    data: {
      status: 'completed',
      completedAt: new Date(),
    },
  });

  console.log(`[Threat Hunt] Hunt ${threatHuntId} completed successfully`);
}


/**
 * Format query results as a readable table for the agent
 */
function formatResultsForAgent(results: any[]): string {
  if (results.length === 0) {
    return 'No results returned from query.';
  }

  // Get all unique keys from results
  const allKeys = Array.from(
    new Set(results.flatMap((r) => Object.keys(r)))
  );

  // Create header row
  const header = allKeys.join(' | ');
  const separator = allKeys.map(() => '---').join(' | ');

  // Create data rows
  const rows = results.map((result) => {
    return allKeys.map((key) => result[key] || '').join(' | ');
  });

  return `${header}\n${separator}\n${rows.join('\n')}`;
}

/**
 * Parse agent output (JSON)
 */
function parseAgentOutput(response: string): ThreatHunterOutput | null {
  try {
    // Try to extract JSON from response (in case there's extra text)
    const jsonMatch = response.match(/\{[\s\S]*\}/);
    if (jsonMatch) {
      return JSON.parse(jsonMatch[0]);
    }

    // Try parsing directly
    return JSON.parse(response);
  } catch (error) {
    console.error(`[Threat Hunt] Failed to parse agent output: ${response.substring(0, 200)}`);
    return null;
  }
}

/**
 * Deduplicate findings and add metadata
 */
async function deduplicateAndProcessFindings(
  threatHuntId: string,
  patternId: string,
  findings: ThreatFinding[]
): Promise<ProcessedThreatFinding[]> {
  // Convert to FindingData format for deduplication
  const findingsData: FindingData[] = findings.map((f) => ({
    finding_type: f.finding_type,
    affected_entities: f.affected_entities,
    detected_at: new Date(),
    time_bucket_hours: 1,
  }));

  // Filter duplicates
  const newFindings = await filterDuplicates(findingsData, threatHuntId);

  // Combine with original finding data
  const processed: ProcessedThreatFinding[] = [];

  for (let i = 0; i < newFindings.length; i++) {
    const original = findings[i];
    const dedupData = newFindings.find(
      (nf) => nf.finding_type === original.finding_type
    );

    if (dedupData) {
      processed.push({
        ...original,
        hash: dedupData.hash,
        isDuplicate: false,
        patternId,
        detectedAt: new Date(),
      });
    }
  }

  return processed;
}

